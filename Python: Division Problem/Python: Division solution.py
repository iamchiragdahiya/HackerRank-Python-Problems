# Check if the Python script is being run as the main program
if __name__ == '__main__':
    # Prompt the user to enter an integer value for 'a' and convert it to an integer
    a = int(input())

    # Prompt the user to enter an integer value for 'b' and convert it to an integer
    b = int(input())

    # Calculate the integer division of 'a' by 'b' (quotient) and print the result
    print(a // b)

    # Calculate the floating-point division of 'a' by 'b' (real number) and print the result
    print(a / b)
